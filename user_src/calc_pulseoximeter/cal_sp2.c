/*==============================================================================*/
/*	cal_sp2																		*/
/*==============================================================================*/
void cal_sp2
(
	double	mx1,		/* IN：赤外最大値										*/
	double	mx2,		/* IN：赤色最大値										*/
	double	*sp			/* OT：SPO2												*/
)
{
	double tmp = mx2/mx1 - 1.0;
	double tmp2 = -0.283* tmp;
	
	*sp = ( tmp2 + 0.83 ) * 100.0;
}
