/*	②ar,aiに対しフィルタリング処理を実施										*/
/*	③逆FFT処理実施																*/
/*	　void ifft( ar , ai , N , ot );											*/
/*==============================================================================*/
/*==============================================================================*/
/* include																		*/
/*==============================================================================*/
#include	<stdio.h>
#include	<stdlib.h>
#include	<math.h>
/*==============================================================================*/
#define		pi	((double)3.1415926535)
/*==============================================================================*/
/*	ifft																		*/
/*==============================================================================*/
void ifft
(
	double	ar[] ,	/* IN：実数部												*/
	double	ai[] ,	/* IN：虚数部												*/
	int		N 	 ,	/* IN：要素数												*/
	double	ot[] 	/* OT：逆生成データ											*/
)
{
	int		n;
	int		k;
	double	Ref;

	for( k=0 ; k<N ; k++ )
	{
		Ref = 0.0;
		for( n=0 ;  n<N ; n++ )
		{
			Ref += ( ar[n]*cos(2*pi*k*n/N ) - ai[n]*sin(2*pi*k*n/N));
		}
		Ref /= ((double)N);
		ot[ k ] = Ref;
	}
}
