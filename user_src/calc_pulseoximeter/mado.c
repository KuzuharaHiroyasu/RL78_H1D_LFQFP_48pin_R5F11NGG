#include	<stdio.h>
#include	<stdlib.h>
#include	<math.h>
/*==============================================================================*/
#define		M_PI	((double)3.1415926535)
/*==============================================================================*/
/*	mado																		*/
/*==============================================================================*/
void	mado( double in[] , double ot[] , int size , int mode )
{
	double winValue = 0;
	int i;

	for( i = 0 ; i < size ; i++ )
	{
		winValue = 0;

		// 各々の窓関数
 		if( mode  == 0 )
		{
			winValue = 
						0.54
						- 0.46 * cos(2 * M_PI * i / (size - 0));
		}
		else if( mode  == 1 )
		{
			winValue =
						0.5
						- 0.5 * cos(2 * M_PI * i / (size - 1));
		}
		else
		{
			winValue = 1.0;
		}

		// 窓関数を掛け算
		ot[i] = in[i] * winValue;
	}
}
